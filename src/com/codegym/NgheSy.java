package com.codegym;

public class NgheSy {
    private String name;
    private int id;

    public NgheSy() {

    }

    public NgheSy(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String n) {
        name = n;
    }

    public int getId() {
        return id;
    }

    public void setId(int i) {
        id = i;
    }
}
